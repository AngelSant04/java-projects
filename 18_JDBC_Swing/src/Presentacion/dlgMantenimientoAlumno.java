    private void tblDatosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblDatosMouseClicked
        int posFila = tblDatos.getSelectedRow();

        txtCodigo.setText(modelo.getValueAt(posFila, 0).toString());
        txtNombres.setText(modelo.getValueAt(posFila, 1).toString());
        txtApPaterno.setText(modelo.getValueAt(posFila, 2).toString());
        txtMaterno.setText(modelo.getValueAt(posFila, 3).toString());
        txtEdad.setText(modelo.getValueAt(posFila, 4).toString());
        for (int i = 1; i < cboEscuela.getItemCount(); i++) {
            clsEscuela obj;
            obj = (clsEscuela)cboEscuela.getItemAt(i);
            if(obj.getIdEscuela() == Integer.parseInt(modelo.getValueAt(posFila, 5).toString())){
                cboEscuela.setSelectedItem(obj);
            }           
        }
        btnModificar.setEnabled(true);
        btnRegistrar.setEnabled(false);
        txtCodigo.setEnabled(false);
    }//GEN-LAST:event_tblDatosMouseClicked

