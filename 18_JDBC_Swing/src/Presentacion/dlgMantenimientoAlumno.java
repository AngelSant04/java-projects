    private void cargarEscuela(){
        ArrayList<clsEscuela> lista;
        clsEscuelaDAO objDAO = new clsEscuelaDAO();
        
        try {
            lista = objDAO.listado();
            for (clsEscuela obj : lista) {
                cboEscuela.addItem(obj);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, e.getMessage());
        }
    }
    
    private void cargarAlumnos(){
        ArrayList<clsAlumno> lista;
        clsAlumnoDAO objDAO = new clsAlumnoDAO();
        
        try {
            lista = objDAO.listado();
            for (clsAlumno obj : lista) {
                String fila[] = {obj.getCodigo(),obj.getNombres(),obj.getApPaterno(),
                obj.getApMaterno(),obj.getEdad()+"",obj.getIdescuela()+""};
                modelo.addRow(fila);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, e.getMessage());
        }
    }
    
