    private void btnRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarActionPerformed
        clsAlumno obj;
        clsAlumnoDAO objDAO = new clsAlumnoDAO();

        try {
              clsEscuela objE = (clsEscuela)cboEscuela.getSelectedItem();              
              obj = new clsAlumno(txtCodigo.getText(), txtNombres.getText(), 
                      txtApPaterno.getText(), txtMaterno.getText(), Integer.parseInt(txtEdad.getText()), 
                      objE.getIdEscuela());
            objDAO.agregar(obj);
            JOptionPane.showMessageDialog(rootPane, "Alumno Agregado");
            modelo.setRowCount(0);
            limpiar();
            cargarAlumnos();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, e.getMessage());
        }
    }//GEN-LAST:event_btnRegistrarActionPerformed

