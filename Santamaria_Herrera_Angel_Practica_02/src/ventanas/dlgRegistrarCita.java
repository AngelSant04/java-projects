    private void formWindowOpened(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowOpened
        cboDni.removeAllItems();
        cboDni.addItem("--SELECCIONE OPCION--");        
        for (int i = 0; i < clsGlobal.coleccionPaciente.size(); i++) {
            cboDni.addItem(clsGlobal.coleccionPaciente.get(i).getDNI());
        }
        cboConsultorio.removeAllItems();
        cboConsultorio.addItem("--SELECCIONE OPCION--");        
        for (int i = 0; i < clsGlobal.coleccionConsultorio.size(); i++) {
            cboConsultorio.addItem(clsGlobal.coleccionConsultorio.get(i).toString());
        }        
    }//GEN-LAST:event_formWindowOpened

    private void cboConsultorioActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cboConsultorioActionPerformed
        String datos[] = cboConsultorio.getSelectedItem().toString().split("-");
        txtEspecialidad.setText(datos[0].trim());
        txtDoctor.setText(datos[1].trim());
        txtEspecialidad.setEnabled(false);
        txtDoctor.setEnabled(false);
    }//GEN-LAST:event_cboConsultorioActionPerformed

    private void btnRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarActionPerformed
               
        for (int i = 0; i < clsGlobal.coleccionConsultorio.size(); i++) {
            if(clsGlobal.coleccionConsultorio.get(i).getCantCitas() != 0){                
                clsGlobal.coleccionConsultorio.get(i).setCantCitas((clsGlobal.coleccionConsultorio.get(i).getCantCitas()-1));
                clsCitas obj = new clsCitas(cboDni.getSelectedItem().toString(),txtEspecialidad.getText(), txtDoctor.getText(),txtPDiagnostico.getText());
            clsGlobal.coleccionCita.add(obj);
            }else{
                JOptionPane.showMessageDialog(rootPane, "NO quedan cupos de Citas");
            }        
        }        
    }//GEN-LAST:event_btnRegistrarActionPerformed

