    void cargarDatosPersonas(){
        for (clsPaciente obj : clsGlobal.coleccionPaciente) {
            modelo.addElement(obj.toString());
        }
    }
    
    private void btnRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarActionPerformed
        clsPaciente obj;

        //validar DNI
        if(clsGlobal.existeDNIRegistrado(txtDNI.getText())){
            JOptionPane.showMessageDialog(rootPane, "DNI Ya registrado");
            return;
        }

        //Validar datos
        if(!txtDNI.getText().equals("") && !txtNombres.getText().equals("")
            && !txtEdad.getText().equals("") && cboSexo.getSelectedIndex()>0 && !txtDistrito.getText().equals("")){

            //EN EL CBO
            //INDEX = POSICION
            //ITEM = EL CONTENIDO

            obj = new clsPaciente(txtDNI.getText(), txtNombres.getText(), Integer.parseInt(txtEdad.getText()), 
                    cboSexo.getSelectedItem().toString(), txtDistrito.getText());

            //lista.add(obj);
            clsGlobal.coleccionPaciente.add(obj);
            JOptionPane.showMessageDialog(rootPane, "Paciente Agregado");
            modelo.clear();
            cargarDatosPersonas();
            limpiar();
        }else{
            JOptionPane.showMessageDialog(rootPane, "Falta ingresar valores");
        }
    }//GEN-LAST:event_btnRegistrarActionPerformed

