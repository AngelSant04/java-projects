    private void btnModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarActionPerformed
        
        clsPaciente obj;

        //Validar datos
        if(!txtDNI.getText().equals("") && !txtNombres.getText().equals("")
            && !txtEdad.getText().equals("") && cboSexo.getSelectedIndex()>0 && !txtDistrito.getText().equals("")){

            //EN EL CBO
            //INDEX = POSICION
            //ITEM = EL CONTENIDO

            obj = new clsPaciente(txtDNI.getText(), txtNombres.getText(), Integer.parseInt(txtEdad.getText()), 
                    cboSexo.getSelectedItem().toString(), txtDistrito.getText());
            
            int pos = lstDatos.getSelectedIndex();

            //lista.add(obj);
            clsGlobal.coleccionPaciente.set(pos,obj);
            JOptionPane.showMessageDialog(rootPane, "Paciente Modificado");
            modelo.clear();
            cargarDatosPersonas();
            limpiar();
        }else{
            JOptionPane.showMessageDialog(rootPane, "Falta ingresar valores");
        }
    }//GEN-LAST:event_btnModificarActionPerformed

