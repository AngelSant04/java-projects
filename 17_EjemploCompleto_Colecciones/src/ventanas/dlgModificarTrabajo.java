    void cargarDatosTrabajo(String dniAux){
        for (clsTrabajo obj : clsGlobal.coleccion) {
            if(obj.getDni().equals(dniAux)){
                modelo.addElement(obj.toString());
            }            
        }
    }
    
    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        modelo.clear();
        if(!clsGlobal.existeDNIRegistrado(txtDNI.getText())){
            JOptionPane.showMessageDialog(rootPane, "DNI no registrado como persona");
            txtDNI.setText("");
        }else{
            cargarDatosTrabajo(txtDNI.getText());
        }        
    }//GEN-LAST:event_btnBuscarActionPerformed

