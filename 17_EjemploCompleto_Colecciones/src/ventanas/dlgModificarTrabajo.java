    private void lstDatosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_lstDatosMouseClicked
        String datos[] = lstDatos.getSelectedValue().split("-");    //Parte la cadena mediante el guion
        txtLabor.setText(datos[1].trim());        //Le retira espacios en blancos a la izquierda y
                                                    //a la derecha (NO EN EL CENTRO)
        txtHoras.setText(datos[2].trim());
        cboArea.setSelectedItem(datos[3].toString().trim());
    }//GEN-LAST:event_lstDatosMouseClicked

    public void limpiar(){
        txtLabor.setText("");
        txtHoras.setText("");
        cboArea.setSelectedIndex(0);
    }
    
    private void btnModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarActionPerformed
        if(!clsGlobal.existeDNIRegistrado(txtDNI.getText())){
            JOptionPane.showMessageDialog(rootPane, "DNI no registrado como persona");
            txtDNI.setText("");
            return;
        }
        
        if(!txtDNI.getText().equals("") && !txtLabor.getText().equals("")
            && !txtHoras.getText().equals("") && cboArea.getSelectedIndex()>0){
            
            String datos[] = lstDatos.getSelectedValue().split("-");    //Parte la cadena mediante el guion            
            String idAux = datos[0].trim();
            clsTrabajo obj = new clsTrabajo(idAux, txtDNI.getText(), txtLabor.getText(),
                Integer.parseInt(txtHoras.getText()), cboArea.getSelectedItem().toString());
                                                                                  
            int pos = clsGlobal.buscarPosicionTrabajo(idAux);
            clsGlobal.coleccion.set(pos, obj);
            JOptionPane.showMessageDialog(rootPane, "Trabajo Modificado");
            modelo.clear();
            cargarDatosTrabajo(txtDNI.getText());
            limpiar();
        }else{
            JOptionPane.showMessageDialog(rootPane, "Falta Ingresar Datos");
        }
    }//GEN-LAST:event_btnModificarActionPerformed

