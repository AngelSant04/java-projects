    private void cboDistritoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cboDistritoActionPerformed
        
        if(cboDistrito.getSelectedIndex() > 0){
            modelo.removeAllElements();     //Elimina todos los elementos de la LISTA
            for (int i = 0; i < clsGlobal.lista.size(); i++) {      //Devuelve la cantidad de terminos
                if(clsGlobal.lista.get(i).getDistrito().equals(cboDistrito.getSelectedItem().toString())){
                    modelo.addElement(clsGlobal.lista.get(i));
                }
            }            
        } 
    }//GEN-LAST:event_cboDistritoActionPerformed

