    private void btnDiagnosticoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDiagnosticoActionPerformed
        clsCitaMedicaDAO objDAO = new clsCitaMedicaDAO();
        
        try {
            if(!txaDiagnostico.getText().equals("")){
                objDAO.modificarDiagnostico(txaDiagnostico.getText(), idSeleccionado);
                JOptionPane.showMessageDialog(rootPane, "Diagnostico Agregado");
                permiteReceta();
                txaDiagnostico.setText("");
                txaDiagnostico.setEnabled(false);
                btnDiagnostico.setEnabled(false);
            }else{
                JOptionPane.showMessageDialog(rootPane, "Falta Ingresar Valores", 
                        "ERROR", JOptionPane.ERROR_MESSAGE, null);
            }            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, e.getMessage());
        }
    }//GEN-LAST:event_btnDiagnosticoActionPerformed

