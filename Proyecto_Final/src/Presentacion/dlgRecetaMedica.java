    private void btnRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarActionPerformed
        clsRecetaMedica obj;
        clsRecetaMedicaDAO objDAO = new clsRecetaMedicaDAO();
        int band = 0;
        String presentacion;
        
        if(rbtnPastilla.isSelected() == true || rbtnJarabe.isSelected() == true || rbtnInyeccion.isSelected() == true){
            band = 1;
        }

        try {
            if(!txtDNI.getText().equals("") && !txaDescripcion.getText().equals("") &&  band == 1){
                if(rbtnPastilla.isSelected()){
                    presentacion = rbtnPastilla.getText();
                } else {
                    if(rbtnJarabe.isSelected()){
                        presentacion = rbtnJarabe.getText();
                    }else presentacion = rbtnInyeccion.getText();
                }
                obj = new clsRecetaMedica(0, txaDescripcion.getText(), presentacion,idSeleccionado);
                objDAO.agregar(obj);
                JOptionPane.showMessageDialog(rootPane, "Receta Agregada");
                cargarRecetas(idSeleccionado);
                limpiar();
            }else{
                JOptionPane.showMessageDialog(rootPane, "Falta Ingresar Valores", 
                        "ERROR", JOptionPane.ERROR_MESSAGE, null);
            }            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, e.getMessage());
        }
    }//GEN-LAST:event_btnRegistrarActionPerformed

