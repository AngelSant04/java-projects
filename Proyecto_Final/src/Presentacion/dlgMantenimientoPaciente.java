    private void cargarPaciente(){
        ArrayList<clsPaciente> lista;
        clsPacienteDAO objDAO = new clsPacienteDAO();
        
        try {
            lista = objDAO.listado();
            for (clsPaciente obj : lista) {
                String fila[] = {obj.getDni(),obj.getIdHistorialClinico(),obj.getNombres(),
                    obj.getApPaterno()+" "+obj.getApMaterno(),obj.getEdad()+"",obj.getSexo(),obj.getTelefono()+""};
                modelo.addRow(fila);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, e.getMessage());
        }
    }
    
