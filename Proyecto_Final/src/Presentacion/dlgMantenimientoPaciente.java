    private void tblDatosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblDatosMouseClicked
        String sexo;
        int posFila = tblDatos.getSelectedRow();
        
        txtDNI.setText(modelo.getValueAt(posFila, 0).toString());
        txtNombres.setText(modelo.getValueAt(posFila, 2).toString());
        String apellidos[] = tblDatos.getValueAt(posFila, 3).toString().split(" ");
        txtApPaterno.setText(apellidos[0].trim());
        txtApMaterno.setText(apellidos[1].trim());
        txtEdad.setText(modelo.getValueAt(posFila, 4).toString());
        sexo = modelo.getValueAt(posFila, 5).toString();
        if(sexo.equals("M")){
            rbtnM.setSelected(rootPaneCheckingEnabled);             //Hacer que se seleccione
        }else rbtnF.setSelected(rootPaneCheckingEnabled);
        txtTelefono.setText(modelo.getValueAt(posFila, 6).toString());
        btnModificar.setEnabled(true);
        btnRegistrar.setEnabled(false);
        txtDNI.setEnabled(false);
    }//GEN-LAST:event_tblDatosMouseClicked

