    private void btnRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarActionPerformed
        clsPaciente obj;
        clsPacienteDAO objDAO = new clsPacienteDAO();
        int band = 0;
        String sexo;
        
        if(rbtnM.isSelected() == true || rbtnF.isSelected() == true){
            band = 1;
        }

        try {
            if(!txtDNI.getText().equals("") && !txtNombres.getText().equals("")
            && !txtApPaterno.getText().equals("") && !txtApMaterno.getText().equals("") 
                    && !txtEdad.getText().equals("") && band == 1 
                    && !txtTelefono.getText().equals("")){
                if(rbtnM.isSelected()){
                    sexo = rbtnM.getText();
                } else sexo = rbtnF.getText();
                obj = new clsPaciente(txtDNI.getText(), txtNombres.getText(), txtApPaterno.getText(), 
                    txtApMaterno.getText(), Integer.parseInt(txtEdad.getText()), sexo, 
                    Integer.parseInt(txtTelefono.getText()), "0P"+txtDNI.getText());
                objDAO.agregar(obj);
                JOptionPane.showMessageDialog(rootPane, "Paciente Agregado");
                modelo.setRowCount(0);            
                cargarPaciente();
                limpiar();
            }else{
                JOptionPane.showMessageDialog(rootPane, "Falta Ingresar Valores", 
                        "ERROR", JOptionPane.ERROR_MESSAGE, null);
            }            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, e.getMessage());
        }
    }//GEN-LAST:event_btnRegistrarActionPerformed

