    private void btnAgregarCitaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarCitaActionPerformed
        clsAtencionMedica objA;
        clsAtencionMedicaDAO objDAO = new clsAtencionMedicaDAO();
        ArrayList<clsCitaMedica> lista = new ArrayList<>();   
        ArrayList<clsServicioMedico> listaServicios = new ArrayList<>(); 
        Date dato = calendario.getDate();
        int band = 0;
        
        try {
            objA = new clsAtencionMedica(0, txtEspecialidad.getText(), txtDNI.getText());
            for (int i = 0; i < tblDatos.getRowCount(); i++) {
                if(IsSelected(i, 3, tblDatos)){ 
                    if(Integer.parseInt(modelo.getValueAt(i, 1).toString()) != 0){
                        SimpleDateFormat fmt = new SimpleDateFormat("yyyy-MM-dd");
                        clsCitaMedica obj = new clsCitaMedica(0, fmt.format(dato), "", 0, 
                            Integer.parseInt(modelo.getValueAt(i, 0).toString()), txtIdDoctor.getText());                    
                        clsServicioMedico objS = new clsServicioMedico(Integer.parseInt(modelo.getValueAt(i, 0).toString()),
                            Integer.parseInt(modelo.getValueAt(i, 1).toString()), modelo.getValueAt(i, 2).toString());
                        listaServicios.add(objS);
                        lista.add(obj);
                        band = 1;
                    }else JOptionPane.showMessageDialog(rootPane, "NO HAY CUPOS DISPONIBLES PARA EL ID "
                            + "SERVICIO "+ modelo.getValueAt(i, 1)+" ESPECIALIDAD: "+modelo.getValueAt(i, 2));                                        
                }
            }
            if(band == 1){
                objDAO.agregar_PA(objA, lista,listaServicios);            
                JOptionPane.showMessageDialog(rootPane, "ATENCION Y CITAS MEDICAS REGISTRADAS");
            }            
            cargarServicios();
            limpiar();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, e.getMessage());
        }
    }//GEN-LAST:event_btnAgregarCitaActionPerformed

