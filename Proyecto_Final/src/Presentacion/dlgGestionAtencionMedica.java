    private void cargarServicios(){
        modelo.setRowCount(0);
        ArrayList<clsServicioMedico> lista;
        clsServicioMedicoDAO objDAO = new clsServicioMedicoDAO();
        
        try {
            lista = objDAO.listado();
            for (clsServicioMedico obj : lista) {
                String fila[] = {obj.getIdServicioMedico()+"",obj.getNumeroCupos()+"",
                    obj.getEspecialidad()};
                modelo.addRow(fila);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, e.getMessage());
        }
    }
    
    public void addcheckBox(int numColum,JTable table){
        TableColumn tc = table.getColumnModel().getColumn(numColum);
        tc.setCellEditor(table.getDefaultEditor(Boolean.class));
        tc.setCellRenderer(table.getDefaultRenderer(Boolean.class));
    }
    
    public boolean IsSelected(int row, int column, JTable table){
        return table.getValueAt(row, column) != null;
    }

